// JS to handle admin notices for customize_remind_later button
jQuery(document).on('click', '.libw_customize_remind_later[data-action="libw_customize_remind_later"]', function (e) {
    e.preventDefault();
    document.cookie = 'libw_customize_remind_later=true; expires=' + new Date(Date.now() + 7 * 24 * 60 * 60 * 1000).toUTCString() + '; path=/';
    jQuery(this).closest('.notice').fadeOut(300, function () {
        jQuery(this).remove();
    });
});

